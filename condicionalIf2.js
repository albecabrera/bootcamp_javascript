// Condicional if
// Aquí tienes un ejemplo básico de cómo usar un condicional if en JavaScript:

let temperatura = 30;

if (temperatura > 25) {
    console.log("Hace calor, mejor usa ropa ligera.");
} else {
    console.log("El clima es fresco, puedes usar ropa más abrigada.");
}

