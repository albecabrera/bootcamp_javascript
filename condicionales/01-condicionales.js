// Condicionales
// if, else
let usuario = 'Juan';

if (usuario == 'Juan') {
    console.log('Autorizado ');
}
else {
    console.log('No autorizado');
}

// Otro ejemplo
let age = 37

if (age == 37) {
    console.log("La edad es 37")
}

// else (si no)

if (age == 37) {
    console.log("La edad es 37")
} else {
    console.log("La edad no es 37")
}
